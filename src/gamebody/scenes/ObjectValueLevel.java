package gamebody.scenes;

/**
 * 物体的4种价值分类
 * <p>{@code HIGH},    最高价值
 * <p>{@code NORMAL},  普通价值
 * <p>{@code LOW}      最低价值
 * <p>{@code SPECIAL}  特殊价值
 */

public enum ObjectValueLevel {

    /**
     * 最高价值
     */
    HIGH,       
 
    /**
     * 普通价值
     */
    NORMAL,     

    /**
     * 最低价值
     */
    LOW,     
       
    /**
     * 特殊价值
     */
    SPECIAL     
}
